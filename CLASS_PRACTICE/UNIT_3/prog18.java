// Local Variable Type Inference Example

package UNIT_3;

public class prog18 {
    public static void main(String[] args) {
        String str1 = "Hello, This is a String!";
        var str2 = "Hello, This is a var String!";
        var str3 = "Hello, This is a var String!";

        System.out.println(str1);
        System.out.println(str2);
        System.out.println(str3);

    }
    
}

// Output:
// Hello, This is a String!
// Hello, This is a var String!
// Hello, This is a var String!